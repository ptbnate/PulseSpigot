package me.nate.spigot.util;

public final class MathUtil {

    private MathUtil() {
    }

    public static int pow2(int value) {
        return value * value;
    }

    public static float pow2(float value) {
        return value * value;
    }

    public static double pow2(double value) {
        return value * value;
    }

}
